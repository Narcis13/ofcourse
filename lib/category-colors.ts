export type CategoryColorScheme = keyof typeof categoryColorSchemes;

export function getCategoryColorScheme(categorySlug: string) {
  const normalizedSlug = categorySlug.toLowerCase().replace(/\s+/g, '-');
  return categoryColorSchemes[normalizedSlug as CategoryColorScheme] || categoryColorSchemes.programming;
}

export function getCategoryColorClass(categorySlug: string, colorType: keyof typeof categoryColorSchemes.programming) {
  const scheme = getCategoryColorScheme(categorySlug);
  return scheme[colorType];
}